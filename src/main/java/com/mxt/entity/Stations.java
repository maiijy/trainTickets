package com.mxt.entity;

public class Stations {
    private Integer stationId;

    private String stationName;

    private String stationAddr;

    private String stationRailway;

    private String stationLng;

    private String stationLat;

    public Integer getStationId() {
        return stationId;
    }

    public void setStationId(Integer stationId) {
        this.stationId = stationId;
    }

    public String getStationName() {
        return stationName;
    }

    public void setStationName(String stationName) {
        this.stationName = stationName;
    }

    public String getStationAddr() {
        return stationAddr;
    }

    public void setStationAddr(String stationAddr) {
        this.stationAddr = stationAddr;
    }

    public String getStationRailway() {
        return stationRailway;
    }

    public void setStationRailway(String stationRailway) {
        this.stationRailway = stationRailway;
    }

    public String getStationLng() {
        return stationLng;
    }

    public void setStationLng(String stationLng) {
        this.stationLng = stationLng;
    }

    public String getStationLat() {
        return stationLat;
    }

    public void setStationLat(String stationLat) {
        this.stationLat = stationLat;
    }
}